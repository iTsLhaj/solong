#include "../libftprintf.h"

int	ft_putstr(char *s)
{
	int	l;

	l = 0;
	if (!s)
	{
		l = 6;
		write(1, "(null)", l);
	}
	else
	{
		while (s[l])
			write(1, &s[l++], 1);
	}
	return (l);
}
